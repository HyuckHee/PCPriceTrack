export const KR_BLACKLIST_KEYWORDS = [
  // ── RAM 노트북 전용 ──────────────────────────────────────────────────────
  'SODIMM', 'SO-DIMM',

  // ── GPU 관련 부품/액세서리 ───────────────────────────────────────────────
  '브래킷', '베젤', '배플', '플레이트', 'IO 실드', 'IO실드', '백플레이트',
  '쿨링 팬', '쿨링팬', '교체 팬', '교체팬', '그래픽카드 팬', '냉각 팬',

  // ── eGPU / 외장 장치 ─────────────────────────────────────────────────────
  'eGPU', 'OCuLink', 'OcuLink', '도크', '도킹',

  // ── 거치/지지 액세서리 ───────────────────────────────────────────────────
  '거치대', '지지대', '서포터', '지지 브라켓',

  // ── 케이블 / 전원 액세서리 ──────────────────────────────────────────────
  '케이블', '전원 공급', '파워 케이블', 'PCIE 커넥터', 'PCIe 커넥터',

  // ── 기타 부품 ────────────────────────────────────────────────────────────
  '구리스', '열전도', '섀시', '인클로저',
] as const;

/**
 * 상품명에 KR_BLACKLIST_KEYWORDS 가 포함되는지 검사.
 * @param title 상품명 (HTML 태그 제거 후 전달 권장)
 */
export function isKrBlacklisted(title: string): boolean {
  const lower = title.toLowerCase();
  return KR_BLACKLIST_KEYWORDS.some((kw) => lower.includes(kw.toLowerCase()));
}
